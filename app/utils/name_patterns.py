COMPILED_NAME_PATTERNS: tuple[tuple[re.Pattern[str], str, int], ...] = tuple(
    (re.compile(item.pattern, flags=re.IGNORECASE), item.replacement, item.priority)
    for item in sorted(RAW_NAME_PATTERNS, key=lambda item: item.priority)
)


def apply_name_patterns(value: str) -> str:
    """Применяет приоритетные паттерны унификации к названию товара."""
    original = str(value or "")
    if not original:
        return ""

    normalized = f" {original.lower().replace('ё', 'е').strip()} "

    for pattern, replacement, _priority in COMPILED_NAME_PATTERNS:
        normalized = pattern.sub(replacement, normalized)

    normalized = re.sub(r"\s+", " ", normalized).strip()
    return normalized